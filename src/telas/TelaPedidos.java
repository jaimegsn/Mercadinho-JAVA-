    private void btnADDDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDDActionPerformed
        if (pedNome.getText().isEmpty() || (int) pedQTD.getValue() == 0) {
            JOptionPane.showMessageDialog(null, "Insira o nome do Pedido e/ou Quantidade");
        } else {

            Pedidos p = new Pedidos();
            p.setNome(pedNome.getText());
            p.setStatus("Pendente");
            p.setQTD((int) pedQTD.getValue());
            p.realizarPedido();
            pedQTD.setValue(0);
            pedNome.setText(null);
        }
    }//GEN-LAST:event_btnADDDActionPerformed

    private void btnExcluirRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirRActionPerformed
        Pedidos p = new Pedidos();
        p.setID(Integer.parseInt(pedID.getText()));
        p.excluirPedido();
        pedNome.setText(null);
        pedID.setText(null);
        btnADDD.setEnabled(true);
    }//GEN-LAST:event_btnExcluirRActionPerformed

    private void limparIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limparIDActionPerformed
        pedID.setText(null);
        pedNome.setText(null);
        pedQTD.setValue(0);
        btnADDD.setEnabled(true);
    }//GEN-LAST:event_limparIDActionPerformed


