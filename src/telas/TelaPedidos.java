    public void pesquisar_pedidos() {
        String sql = "select * from pedidos where nome like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, campoPedido.getText() + "%");
            rs = pst.executeQuery();
            tabelaProduto.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    public void setar_campos() {
        int setar = tabelaProduto.getSelectedRow();
        pedID.setText(tabelaProduto.getModel().getValueAt(setar, 0).toString());
        pedNome.setText(tabelaProduto.getModel().getValueAt(setar, 1).toString());
        pedQTD.setValue(tabelaProduto.getModel().getValueAt(setar, 2));

    }

